dp = [0 for _ in range(10001)]

cnt = 1

for i in range(1, 10001):
    if not dp[i]:
        for j in range(int(10000**0.5)):
            for k in range(1, int(10000**0.5)+1):
                if k**2 + j < 10001 and not dp[k**2 + j]:
                    dp[k**2 + j] = cnt
            cnt += 1

print(dp)

